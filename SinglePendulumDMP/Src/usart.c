/**
  * @brief  Retargets the C library printf function to the USART.
  * @param  None
  * @retval None
  */
int SendChar (int ch) 
{
	while (!(USART2->SR & UART_FLAG_TXE))
	{ }		// USART2可换为其他通信串口
  USART2->DR = (ch & 0x1FF);
  return (ch);
}

int GetKey (void) 
{
	while (!(USART2->SR & UART_FLAG_RXNE ));
	return ((int)(USART2->DR & 0x1FF));
}


